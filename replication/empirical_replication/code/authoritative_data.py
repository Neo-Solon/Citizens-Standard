def real_sp500_return(year):
    """Fisher equation: (1 + nominal)/(1 + CPI) - 1."""
    nom = SP500_NOMINAL[year] / 100.0
    cpi = CPI_DECDEC[year] / 100.0
    return (1 + nom) / (1 + cpi) - 1


def joint_triples(start_year, end_year):
    """
    Return list of (real_return, CPI_decdec_pct, real_gdp_growth_pct) tuples
    for years in [start_year, end_year] inclusive. The fundamental joint
    distribution used by the Monte Carlo resampler.
    """
    out = []
    for y in range(start_year, end_year + 1):
        if y not in CPI_DECDEC or y not in SP500_NOMINAL or y not in REAL_GDP_GROWTH:
            continue
        out.append((
            real_sp500_return(y),
            CPI_DECDEC[y],
            REAL_GDP_GROWTH[y],
        ))
    return out


